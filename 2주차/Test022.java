public class Test023
{
	public static void main(String args[])
	{
		int a = 10, b = -10;

		System.out.printf("~a : %d%n", ~a); //비트낫 
		System.out.printf("~b : %d%n", ~b);

	}
}
// 실행 결과
/*
~a : -11
~b : 9
*/

